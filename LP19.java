public class LP19 {

	public static void main(String[] args) {

		int i = 3;
		int j = 2;
				
		
		boolean evenNumber;
		if (i % 2 == 0) {
			evenNumber = true;
		} else {
			evenNumber = false;
		}
		
		System.out.println(evenNumber);
		
		if (j % 2 == 0) {
			evenNumber = true;
		} else {
			evenNumber = false;
		}
		
		System.out.println(evenNumber);
		
	}

}
